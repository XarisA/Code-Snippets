DECLARE @P4th varchar(80)
DECLARE @DB nvarchar(20)

SELECT @P4th = (SELECT 'PATH\FILE.' + 
convert(varchar(80),GetDate(),102) + '.bak') 
SET @DB='FILE'

BACKUP DATABASE @DB TO DISK=@P4th
GO
