-- Query to back up Database

BACKUP DATABASE TF_TRANSLATIONS TO DISK = N'PATH\FILE.BAK'
WITH NOFORMAT, INIT, NAME = N'DB1' , SKIP, NOREWIND, NOUNLOAD, STATS = 10

BACKUP DATABASE TFDB_VIORYL TO DISK = N'PATH\FILE.BAK'
WITH NOFORMAT, INIT, NAME = N'DB2' , SKIP, NOREWIND, NOUNLOAD, STATS = 10

GO

